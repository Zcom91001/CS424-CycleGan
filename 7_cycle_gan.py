# ------------------------------------------------------------
# 2) Define a custom Dataset that returns (apple_img, orange_img)
# ------------------------------------------------------------
# CycleGAN uses *unpaired* image-to-image translation:
# - We do NOT need aligned (apple_i, orange_i) pairs.
# - Instead, during training we sample an apple image from domain A
#   and an orange image from domain B independently.
#
# This dataset class does exactly that by indexing with modulo:
# - apple index cycles through trainA images
# - orange index cycles through trainB images
# and the length is max(len(A), len(B)) so an epoch covers the larger domain.
class Apple2OrangeDataset(torch.utils.data.Dataset):
    def __init__(self, root_dir, transform=None, train_or_test='train'):
        """
        Args:
            root_dir (str): Path that contains the extracted 'apple2orange' folder.
            transform (callable): torchvision transforms applied to both domains.
            train_or_test (str): 'train' or 'test' to load the corresponding split.
        """
        # Domain A: apples, Domain B: oranges (following CycleGAN naming convention)
        self.apple_path = os.path.join(root_dir, f'apple2orange/{train_or_test}A')
        self.orange_path = os.path.join(root_dir, f'apple2orange/{train_or_test}B')
        self.transform = transform

        # List image filenames in each domain directory
        self.apple_images = sorted(os.listdir(self.apple_path))
        self.orange_images = sorted(os.listdir(self.orange_path))

        # Lengths of each domain
        self.apple_len = len(self.apple_images)
        self.orange_len = len(self.orange_images)

        # Define dataset length:
        self.length_dataset = max(self.apple_len, self.orange_len)

    def __len__(self):
        return self.length_dataset

    def __getitem__(self, index):
        """
        Returns:
            apple_array  (Tensor): one image from domain A (apples)
            orange_array (Tensor): one image from domain B (oranges)

        Note:
        - Images are unpaired: apple and orange returned here are not corresponding scenes.
        - Modulo (%) enables cycling through smaller domain without IndexError.
        """
        apple_image = self.apple_images[index % self.apple_len]
        orange_image = self.orange_images[index % self.orange_len]

        apple_image_path = os.path.join(self.apple_path, apple_image)
        orange_image_path = os.path.join(self.orange_path, orange_image)

        apple_array = Image.open(apple_image_path).convert("RGB")
        orange_array = Image.open(orange_image_path).convert("RGB")

        if self.transform:
            apple_array = self.transform(apple_array)
            orange_array = self.transform(orange_array)

        return apple_array, orange_array

