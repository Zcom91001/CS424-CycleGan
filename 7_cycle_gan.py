# ------------------------------------------------------------
# CycleGAN-style building blocks (Generator + Patch Discriminator)
# ------------------------------------------------------------
# This code implements:
# 1) A PatchGAN discriminator: outputs a grid of probabilities (patch-level realism),
#    not a single scalar. This encourages high-frequency realism (texture/details).
# 2) A ResNet-based generator: encoder (downsample) -> residual blocks -> decoder (upsample).
#
# Important conventions here:
# - Input/output images are normalized to [-1, 1] (so generator ends with Tanh()).
# - InstanceNorm is used instead of BatchNorm (common in style transfer / CycleGAN)
#   because it is more stable for small batch sizes (often batch=1 in CycleGAN).
# - padding_mode='reflect' reduces edge artifacts compared to zero padding.
#
# NETWORK_FEATURES is a global hyperparameter (base channel count), e.g., 64.
# ------------------------------------------------------------


# ============================================================
# Discriminator blocks
# ============================================================

class DBlock(nn.Module):
    """
    One discriminator block: Conv -> (optional) InstanceNorm -> LeakyReLU.

    - Conv2d extracts features and (usually) downsamples when stride=2.
    - InstanceNorm2d normalizes per-sample (not across batch), stable for small batches.
    - LeakyReLU keeps gradients for negative activations (helps discriminator training).

    first_block=True skips InstanceNorm, which is a common PatchGAN design choice:
    the first layer often omits normalization to preserve raw signal statistics.
    """
    def __init__(self, in_channels, out_channels, first_block=False, **kwargs):
        super().__init__()
        self.block = nn.Sequential(
            # padding_mode='reflect' reduces border artifacts in image-to-image tasks
            nn.Conv2d(in_channels, out_channels, **kwargs, padding_mode='reflect', bias=True),

            # Skip normalization in the first layer (common practice)
            nn.InstanceNorm2d(out_channels) if not first_block else nn.Identity(),

            nn.LeakyReLU(0.2, inplace=True)
        )

    def forward(self, x):
        return self.block(x)


class Discriminator(nn.Module):
    """
    PatchGAN Discriminator:
    - Input: (B, 3, 256, 256)
    - Output: (B, 1, ~30, ~30) probability map
      Each element corresponds to a "patch" decision ("real" vs "fake") for a receptive field.
    """
    def __init__(self):
        super().__init__()

        # Feature extractor part (mostly downsampling)
        self.model = nn.Sequential(
            # (B, 3, 256, 256) -> (B, 64, 128, 128)
            DBlock(3, NETWORK_FEATURES, first_block=True, kernel_size=4, stride=2, padding=1),

            # (B, 64, 128, 128) -> (B, 128, 64, 64)
            DBlock(NETWORK_FEATURES*1, NETWORK_FEATURES*2, kernel_size=4, stride=2, padding=1),

            # (B, 128, 64, 64) -> (B, 256, 32, 32)
            DBlock(NETWORK_FEATURES*2, NETWORK_FEATURES*4, kernel_size=4, stride=2, padding=1),

            # stride=1 here: stop aggressive downsampling; increase receptive field while
            # keeping a reasonably dense patch grid.
            # (B, 256, 32, 32) -> (B, 512, 31, 31)
            DBlock(NETWORK_FEATURES*4, NETWORK_FEATURES*8, kernel_size=4, stride=1, padding=1),
        )

        # Final classifier head:
        # (B, 512, 31, 31) -> (B, 1, 30, 30)
        # Sigmoid maps to [0,1] probabilities.
        self.last_layer = nn.Sequential(
            nn.Conv2d(NETWORK_FEATURES*8, 1, kernel_size=4, stride=1, padding=1, padding_mode="reflect"),
            nn.Sigmoid()
        )

        # Teaching note:
        # If you use nn.BCEWithLogitsLoss, you should REMOVE Sigmoid and output logits instead,
        # which is numerically more stable.

    def forward(self, x):
        # Safety reshape: expects flattened input sometimes.
        x = x.view(-1, 3, 256, 256)

        x = self.model(x)        # patch feature maps
        x = self.last_layer(x)   # patch probabilities
        return x


# ============================================================
# Generator blocks
# ============================================================

class GBlock(nn.Module):
    """
    A generator block used for both downsampling and upsampling.

    down=True  -> Conv2d (usually stride=2) to reduce spatial size (encoder)
    down=False -> ConvTranspose2d to increase spatial size (decoder)

    InstanceNorm + ReLU is standard in CycleGAN generators.
    use_act=False is used in residual blocks' second conv to keep the residual path "linear-ish".
    """
    def __init__(self, in_channels, out_channels, down=True, use_act=True, **kwargs):
        super().__init__()
        self.conv = nn.Sequential(
            # Downsample: standard conv with reflect padding to avoid edge artifacts
            nn.Conv2d(in_channels, out_channels, **kwargs, padding_mode="reflect") if down else
            # Upsample: transposed conv (a learnable upsampling)
            nn.ConvTranspose2d(in_channels, out_channels, **kwargs),

            nn.InstanceNorm2d(out_channels),

            # Activation: ReLU for most layers; Identity in some places (e.g., residual tail)
            nn.ReLU(inplace=True) if use_act else nn.Identity()
        )

    def forward(self, x):
        return self.conv(x)


class ResidualBlock(nn.Module):
    """
    ResNet block: x -> Conv -> ReLU -> Conv -> +x

    Both convs keep spatial size fixed (kernel=3, stride=1, padding=1).
    """
    def __init__(self, channels):
        super().__init__()
        self.block = nn.Sequential(
            # First conv with activation
            GBlock(channels, channels, use_act=True, kernel_size=3, stride=1, padding=1),

            # Second conv without activation (so residual addition is not overly squashed)
            GBlock(channels, channels, use_act=False, kernel_size=3, stride=1, padding=1)
        )

    def forward(self, x):
        # Skip connection: preserves input information and stabilizes training
        return x + self.block(x)


class Generator(nn.Module):
    """
    CycleGAN Generator (ResNet-based):
      Input  (B, 3, 256, 256)
        -> initial 7x7 conv
        -> 2x downsampling (to 64x64)
        -> N residual blocks (default 9 for 256x256 images in the original CycleGAN paper)
        -> 2x upsampling (back to 256x256)
        -> final 7x7 conv + Tanh

    Output is in [-1, 1], matching Normalize(mean=0.5,std=0.5).
    """
    def __init__(self, num_residuals=9):
        super().__init__()

        # Initial feature extraction:
        # Large 7x7 kernel captures wider context early.
        # (B, 3, 256, 256) -> (B, 64, 256, 256)
        self.initial = nn.Sequential(
            nn.Conv2d(3, NETWORK_FEATURES, kernel_size=7, stride=1, padding=3, padding_mode="reflect"),
            nn.ReLU(inplace=True),
        )

        # Encoder / downsampling:
        # Each stride=2 halves H and W, doubles channels
        self.down_block = nn.Sequential(
            # (B, 64, 256, 256) -> (B, 128, 128, 128)
            GBlock(NETWORK_FEATURES*1, NETWORK_FEATURES*2, kernel_size=3, stride=2, padding=1),

            # (B, 128, 128, 128) -> (B, 256, 64, 64)
            GBlock(NETWORK_FEATURES*2, NETWORK_FEATURES*4, kernel_size=3, stride=2, padding=1),
        )

        # Transformer / bottleneck (ResNet blocks at 64x64 resolution)
        # 9 residual blocks is standard for 256x256 training (often 6 for 128x128).
        self.residual_block = nn.Sequential(
            *[ResidualBlock(NETWORK_FEATURES*4) for _ in range(num_residuals)]
        )

        # Decoder / upsampling:
        # Use ConvTranspose2d blocks to return to original resolution
        self.up_block = nn.Sequential(
            # (B, 256, 64, 64) -> (B, 128, 128, 128)
            GBlock(
                NETWORK_FEATURES*4, NETWORK_FEATURES*2,
                down=False, kernel_size=3, stride=2, padding=1, output_padding=1
            ),

            # (B, 128, 128, 128) -> (B, 64, 256, 256)
            GBlock(
                NETWORK_FEATURES*2, NETWORK_FEATURES*1,
                down=False, kernel_size=3, stride=2, padding=1, output_padding=1
            ),
        )

        # Final projection back to RGB image space:
        # Tanh constrains output to [-1, 1]
        self.last = nn.Sequential(
            nn.Conv2d(NETWORK_FEATURES, 3, kernel_size=7, stride=1, padding=3, padding_mode="reflect"),
            nn.Tanh()
        )

    def forward(self, x):
        x = self.initial(x)
        x = self.down_block(x)
        x = self.residual_block(x)
        x = self.up_block(x)
        x = self.last(x)
        return x


