# ------------------------------------------------------------
# 7) Instantiate models (CycleGAN has 2 generators + 2 discriminators)
# ------------------------------------------------------------
# CycleGAN learns *bidirectional* translation between two domains:
#   Domain A (apples)  <-->  Domain B (oranges)
#
# Generators:
#   gen_O : A -> B  (given an apple image, generate an orange-like image)
#   gen_A : B -> A  (given an orange image, generate an apple-like image)
#
# Discriminators (PatchGAN):
#   disc_A : judges whether an image looks like a real apple-domain image
#   disc_O : judges whether an image looks like a real orange-domain image
disc_A = Discriminator().to(DEVICE)  # Classify apple-domain images as real or fake
disc_O = Discriminator().to(DEVICE)  # Classify orange-domain images as real or fake
gen_A  = Generator().to(DEVICE)      # Generator for mapping B -> A
gen_O  = Generator().to(DEVICE)      # Generator for mapping A -> B


# ------------------------------------------------------------
# 8) Weight initialization
# ------------------------------------------------------------
# Apply our custom init to all layers in each network.
# This can reduce training instability, especially for GANs.
disc_A.apply(weights_init)
disc_O.apply(weights_init)
gen_A.apply(weights_init)
gen_O.apply(weights_init)


# ------------------------------------------------------------
# 9) Optimizers
# ------------------------------------------------------------
# CycleGAN commonly uses Adam with betas=(0.5, 0.999) (GAN-standard choice).
# We use:
# - one optimizer for BOTH generators (gen_A + gen_O)
# - one optimizer for BOTH discriminators (disc_A + disc_O)
#
# This is convenient because the generator losses are coupled (cycle + identity).
gen_optimizer  = torch.optim.Adam(
    list(gen_A.parameters()) + list(gen_O.parameters()),
    lr=LR,
    betas=(0.5, 0.999)
)
disc_optimizer = torch.optim.Adam(
    list(disc_A.parameters()) + list(disc_O.parameters()),
    lr=LR,
    betas=(0.5, 0.999)
)


# ------------------------------------------------------------
# 10) Loss logging for plotting (per iteration)
# ------------------------------------------------------------
loss_log = {
    'time': [],   # wall-clock time per iteration (for speed profiling)
    'd_loss': [], # discriminator loss values
    'g_loss': []  # generator loss values
}


# ------------------------------------------------------------
# 11) Training loop
# ------------------------------------------------------------
# We alternate updates:
#   (1) update discriminators to better distinguish real vs fake
#   (2) update generators to fool discriminators + satisfy cycle/identity constraints
#
# Important: this implementation uses least-squares GAN loss (LSGAN):
# - discriminator uses MSE to targets 1 (real) and 0 (fake)
# - generator uses MSE to target 1 (want discriminator to say "real")
# This matches the original CycleGAN paper (often more stable than BCE).
for epoch in range(EPOCH):

    # Put networks in training mode:
    # - enables dropout (if any) and sets norms to training behavior
    disc_A.train()
    disc_O.train()
    gen_A.train()
    gen_O.train()

    loop = tqdm(dataloader, total=len(dataloader))
    loop.set_description(f'Epoch [{epoch + 1}/{EPOCH}]')

    # --------------------------------------------------------
    # Learning rate schedule: linear decay after halfway point
    # --------------------------------------------------------
    # CycleGAN typically keeps LR constant for first half of training,
    # then linearly decays to 0 in the second half.
    if epoch >= EPOCH / 2:
        decay = (EPOCH - epoch) / (EPOCH / 2)   # goes from 1 -> 0
        gen_optimizer.param_groups[0]['lr']  = LR * decay
        disc_optimizer.param_groups[0]['lr'] = LR * decay

    for i, data in enumerate(loop):
        start_time = time.time()

        # Each batch returns unpaired samples:
        # real_apple  from domain A, shape (B, 3, 256, 256)
        # real_orange from domain B, shape (B, 3, 256, 256)
        real_apple  = data[0].to(DEVICE)
        real_orange = data[1].to(DEVICE)

        # ====================================================
        # (A) Discriminator update
        # ====================================================
        # Generate fake images:
        # - fake_apple: B -> A  (turn oranges into apple-like)
        # - fake_orange: A -> B (turn apples into orange-like)
        #
        # detach() is critical when training discriminators:
        # - it stops gradients from flowing into the generators
        # - otherwise you'd accidentally update generators during D step
        fake_apple  = gen_A(real_orange)
        fake_orange = gen_O(real_apple)

        # --- Discriminator A (apple-domain) ---
        real_A_preds = disc_A(real_apple)           # should be close to 1
        fake_A_preds = disc_A(fake_apple.detach())  # should be close to 0

        # LSGAN losses (MSE):
        D_real_A_loss = F.mse_loss(real_A_preds, torch.ones_like(real_A_preds))
        D_fake_A_loss = F.mse_loss(fake_A_preds, torch.zeros_like(fake_A_preds))
        D_A_loss = D_real_A_loss + D_fake_A_loss

        # --- Discriminator O (orange-domain) ---
        real_O_preds = disc_O(real_orange)            # should be close to 1
        fake_O_preds = disc_O(fake_orange.detach())   # should be close to 0

        D_real_O_loss = F.mse_loss(real_O_preds, torch.ones_like(real_O_preds))
        D_fake_O_loss = F.mse_loss(fake_O_preds, torch.zeros_like(fake_O_preds))
        D_O_loss = D_real_O_loss + D_fake_O_loss

        # Average the two discriminator losses (optional scaling)
        D_loss = (D_A_loss + D_O_loss) / 2

        disc_optimizer.zero_grad()
        D_loss.backward()
        disc_optimizer.step()


        # ====================================================
        # (B) Generator update
        # ====================================================
        # GAN loss for generators:
        # - want discriminators to classify generated images as "real" (target=1)
        fake_A_preds = disc_A(fake_apple)   # now WITHOUT detach so gradients flow to gen_A
        fake_O_preds = disc_O(fake_orange)  # gradients flow to gen_O

        G_A_loss = F.mse_loss(fake_A_preds, torch.ones_like(fake_A_preds))
        G_O_loss = F.mse_loss(fake_O_preds, torch.ones_like(fake_O_preds))

        # ---------------------
        # Cycle-consistency loss
        # ---------------------
        # Enforce that translating there-and-back reconstructs the original:
        #   A -> B -> A ≈ A
        #   B -> A -> B ≈ B
        #
        # L1 is typically used (less blurring than L2).
        cycle_orange = gen_O(fake_apple)   # B->A->B reconstruction
        cycle_apple  = gen_A(fake_orange)  # A->B->A reconstruction

        consistency_A_loss = F.l1_loss(cycle_apple,  real_apple)
        consistency_O_loss = F.l1_loss(cycle_orange, real_orange)

        # -------------
        # Identity loss
        # -------------
        # Encourage generators to be near-identity when input is already in target domain:
        #   gen_A(A) ≈ A and gen_O(B) ≈ B
        #
        # This helps preserve color composition and prevents unnecessary changes.
        identity_A = gen_A(real_apple)
        identity_O = gen_O(real_orange)

        identity_A_loss = F.l1_loss(identity_A, real_apple)
        identity_O_loss = F.l1_loss(identity_O, real_orange)

        # Combine losses with standard CycleGAN weights:
        # - cycle loss weight often = 10
        # - identity loss weight often = 5 (sometimes 0.5*lambda_cycle in papers)
        G_loss = (
            G_A_loss + G_O_loss
            + (consistency_A_loss + consistency_O_loss) * 10.0
            + (identity_A_loss + identity_O_loss) * 5.0
        )

        gen_optimizer.zero_grad()
        G_loss.backward()
        gen_optimizer.step()


        # ====================================================
        # (C) Logging + tqdm display
        # ====================================================
        loss_log['d_loss'].append(D_loss.item())
        loss_log['g_loss'].append(G_loss.item())
        loss_log['time'].append(time.time() - start_time)

        loop.set_postfix(d_loss=D_loss.item(), g_loss=G_loss.item())


    # --------------------------------------------------------
    # 12) Save sample validation images every epoch
    # --------------------------------------------------------
    # We periodically translate a few fixed test images to monitor progress.
    # The saved grid is: [real_A | fake_O | real_O | fake_A] for several chosen examples.
    if (epoch + 1) % 1 == 0:
        with torch.no_grad():

            # Hand-picked indices for qualitative tracking across epochs
            # Each tuple selects (apple_index, orange_index) from the test split.
            eval_image_list = [(15, 13), (39, 39), (153, 156), (170, 233)]
            all_grids = []

            for i in range(len(eval_image_list)):
                # test_dataset returns (apple, orange) but we intentionally mix indices here
                # to keep domains unpaired even in visualization.
                real_A = test_dataset[eval_image_list[i][0]][0]
                real_O = test_dataset[eval_image_list[i][1]][1]

                # Add batch dimension: (C,H,W) -> (1,C,H,W)
                real_A = real_A[None].to(DEVICE)
                real_O = real_O[None].to(DEVICE)

                # Translate:
                # - fake_O: apple -> orange
                # - fake_A: orange -> apple
                fake_O = gen_O(real_A)
                fake_A = gen_A(real_O)

                # Convert from normalized torch tensors to numpy images in [0,1]
                # Because training normalized to [-1,1], undo by: (x + 1) / 2
                real_A = (real_A[0].cpu().numpy() + 1) / 2
                fake_O = (fake_O[0].cpu().numpy() + 1) / 2
                real_O = (real_O[0].cpu().numpy() + 1) / 2
                fake_A = (fake_A[0].cpu().numpy() + 1) / 2

                # Build a horizontal strip: [real_A | fake_O | real_O | fake_A]
                # axis=2 concatenates along width because tensors are (C, H, W)
                grid = np.concatenate([real_A, fake_O, real_O, fake_A], axis=2)

                # Convert (C,H,W) -> (H,W,C) for saving as an image
                grid = np.transpose(grid, (1, 2, 0))
                all_grids.append(grid)

            # Stack strips vertically to form a final grid image
            final_grid = np.vstack(all_grids)

            # Save as PNG:
            # - scale [0,1] -> [0,255]
            # - uint8 required for PIL image saving
            final_grid_img = Image.fromarray((final_grid * 255).astype(np.uint8))
            final_grid_img.save(os.path.join(OUTPUT_PATH, f"validated_imgs/epoch_{epoch+1}.png"))


    # --------------------------------------------------------
    # 13) Save loss log every epoch
    # --------------------------------------------------------
    # Pickle is convenient for quick reload, but not ideal for long-term portability.
    with open(os.path.join(OUTPUT_PATH, 'loss_log.pkl'), 'wb') as f:
        pickle.dump(loss_log, f)


    # --------------------------------------------------------
    # 14) Save model checkpoints periodically
    # --------------------------------------------------------
    # Saving state_dict() stores parameters only (standard PyTorch approach).
    # If you want resume training exactly, also save optimizers and current epoch.
    if (epoch + 1) % CHECKPOINT_SAVE_EVERY == 0 or epoch == EPOCH - 1:
        torch.save(gen_A.state_dict(),  os.path.join(OUTPUT_PATH, f'checkpoints/gen_A_epoch_{epoch+1:02d}.pth'))
        torch.save(gen_O.state_dict(),  os.path.join(OUTPUT_PATH, f'checkpoints/gen_O_epoch_{epoch+1:02d}.pth'))
        torch.save(disc_A.state_dict(), os.path.join(OUTPUT_PATH, f'checkpoints/disc_A_epoch_{epoch+1:02d}.pth'))
        torch.save(disc_O.state_dict(), os.path.join(OUTPUT_PATH, f'checkpoints/disc_O_epoch_{epoch+1:02d}.pth'))


